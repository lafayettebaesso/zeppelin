package br.com.ctesop.to;

public class ReceitaTO {

    private int idReceita;
    private String nomeReceita;
    private String rendimentoTotalReceita;
    private String modoFazerReceita;
    private String tempoCozimentoReceita;
    private String temperaturaReceita;
    private float custoReceita;

    public int getIdReceita() {
        return idReceita;
    }

    public void setIdReceita(int idReceita) {
        this.idReceita = idReceita;
    }

    public String getNomeReceita() {
        return nomeReceita;
    }

    public void setNomeReceita(String nomeReceita) {
        this.nomeReceita = nomeReceita;
    }

    public String getRendimentoTotalReceita() {
        return rendimentoTotalReceita;
    }

    public void setRendimentoTotalReceita(String rendimentoTotalReceita) {
        this.rendimentoTotalReceita = rendimentoTotalReceita;
    }

    public String getModoFazerReceita() {
        return modoFazerReceita;
    }

    public void setModoFazerReceita(String modoFazerReceita) {
        this.modoFazerReceita = modoFazerReceita;
    }

    public String getTempoCozimentoReceita() {
        return tempoCozimentoReceita;
    }

    public void setTempoCozimentoReceita(String tempoCozimentoReceita) {
        this.tempoCozimentoReceita = tempoCozimentoReceita;
    }

    public String getTemperaturaReceita() {
        return temperaturaReceita;
    }

    public void setTemperaturaReceita(String temperaturaReceita) {
        this.temperaturaReceita = temperaturaReceita;
    }

    public float getCustoReceita() {
        return custoReceita;
    }

    public void setCustoReceita(float custoReceita) {
        this.custoReceita = custoReceita;
    }
    
    
}
