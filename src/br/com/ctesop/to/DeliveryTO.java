package br.com.ctesop.to;

public class DeliveryTO {

    private int idDelivery;
    private String logradouroDelivery;
    private int numeroDelivery;
    private String bairroDelivery;
    private String referenciaDelivery;

    public int getIdDelivery() {
        return idDelivery;
    }

    public void setIdDelivery(int idDelivery) {
        this.idDelivery = idDelivery;
    }

    public String getLogradouroDelivery() {
        return logradouroDelivery;
    }

    public void setLogradouroDelivery(String logradouroDelivery) {
        this.logradouroDelivery = logradouroDelivery;
    }

    public int getNumeroDelivery() {
        return numeroDelivery;
    }

    public void setNumeroDelivery(int numeroDelivery) {
        this.numeroDelivery = numeroDelivery;
    }

    public String getBairroDelivery() {
        return bairroDelivery;
    }

    public void setBairroDelivery(String bairroDelivery) {
        this.bairroDelivery = bairroDelivery;
    }

    public String getReferenciaDelivery() {
        return referenciaDelivery;
    }

    public void setReferenciaDelivery(String referenciaDelivery) {
        this.referenciaDelivery = referenciaDelivery;
    }
}
